def f(lampadas_acessas):
    casas_iluminadas = [0] * len(lampada_casa)
    qtd_lampadas_acessas = 0

    for i, lampada in enumerate(lampadas_acessas):
        if lampada == 1:
            qtd_lampadas_acessas += 1
            linha_lampada = lampada_casa[i]

            for j, casa in enumerate(linha_lampada):
                if casa == 1:
                    casas_iluminadas[j] = 1

    qtd_casas_iluminadas = sum(list(filter(lambda x: x == 1, casas_iluminadas)))
    if qtd_casas_iluminadas < len(lampada_casa):
        return len(lampada_casa) + 1

    return qtd_lampadas_acessas


class Estado:
    def __init__(self, lampadas_acessas):
        self.lampadas_acessas = lampadas_acessas
        self.f = f(lampadas_acessas)


def estado_inicial():
    lampadas_acessas = [
        random.choice([0, 1])
        for _ in range(50)
    ]

    return Estado(lampadas_acessas)


def exibe(estado):
    print(f"Lampadas acessas: {estado.lampadas_acessas}, f(x): {estado.f}")


def melhor_sucessor(estado):
    melhor = estado
    for _ in range(10):
        posicao = random.randint(0, len(lampada_casa) - 1)
        lampadas_acessas = estado.lampadas_acessas.copy()

        lampadas_acessas[posicao] = 1 - lampadas_acessas[posicao]
        vizinho = Estado(lampadas_acessas)
        if vizinho.f < melhor.f:
            melhor = vizinho

    return melhor


def cruzamento(estado1, estado2):
    lampadas_acessas = []
    for acessa1, acessa2 in zip(
        estado1.lampadas_acessas, estado2.lampadas_acessas
    ):
        lampadas_acessas.append(random.choice([acessa1, acessa2]))

    return Estado(lampadas_acessas)


melhor = estado_inicial()

while True:
    atual = estado_inicial()
    while True:
        vizinho = melhor_sucessor(atual)
        if vizinho.f >= atual.f:
            break
        atual = vizinho

    if atual.f < melhor.f:
        melhor = atual
        exibe(melhor)

print("Melhor solução:")
exibe(melhor)
