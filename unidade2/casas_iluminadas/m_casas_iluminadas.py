def f(ligadas):
    for j in range(len(lampada_casa[0])):
        iluminada = False
        for i in range(len(lampada_casa)):
            if lampada_casa[i][j] and ligadas[i]:
                iluminada = True
                break

        if not iluminada:
            return len(ligadas) + 1

    cont = 0
    for ligada in ligadas:
        if ligada:
            cont += 1

    return cont


class Estado:
    def __init__(self, ligadas):
        self.ligadas = ligadas
        self.f = f(ligadas)


def exibe(estado):
    print(f"Lampadas: {estado.ligadas}, f:{estado.f}")


def estado_inicial():
    ligadas = [True] * len(lampada_casa)
    return Estado(ligadas)


def mutacao(estado):
    ligadas = estado.ligadas.copy()
    i = random.randint(0, len(lampada_casa)) - 1
    ligadas[i] = not ligadas[i]

    return Estado(ligadas)


def melhor_sucessor(estado):
    melhor = estado
    for _ in range(10):
        posicao = random.randint(0, len(lampada_casa) - 1)
        lampadas_acessas = estado.ligadas.copy()

        lampadas_acessas[posicao] = 1 - lampadas_acessas[posicao]
        vizinho = Estado(lampadas_acessas)
        if vizinho.f < melhor.f:
            melhor = vizinho

    return melhor


def cruzamento(estado1, estado2):
    ligadas = []
    for i in range(len(estado1.ligadas)):
        gene = random.choice([estado1.ligadas[i], estado2.ligadas[i]])
        ligadas.append(gene)

    return Estado(ligadas)


melhor = estado_inicial()

while True:
    atual = estado_inicial()
    while True:
        vizinho = melhor_sucessor(atual)
        if vizinho.f >= atual.f:
            break
        atual = vizinho

    if atual.f < melhor.f:
        melhor = atual
        exibe(melhor)
